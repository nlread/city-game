public class CellGroup
{
    private int size;
    private int x;
    private int y;
    public CellGroup(int size, int x, int y)
    {
        this.size = size;
        this.x = x;
        this.y = y;
    }

    public int getSize()
    {
        return size;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
